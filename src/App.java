    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int ndx = jTable1.getSelectedRow();
        if (ndx == -1) return;
        
        Element secilenHaber = haberListesi.get(ndx);
        
        jEditorPane1.setContentType("text/html");
        jEditorPane1.setText(secilenHaber.select("description").text());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ndx = jTable1.getSelectedRow();
        if (ndx == -1) return;
        
        Element secilenHaber = haberListesi.get(ndx);
        String haberAdresi = secilenHaber.select("guid").text();
        
        try
        {
            Desktop.getDesktop().browse(URI.create(haberAdresi));
        } catch (Exception e) { e.printStackTrace(); }
        
    }//GEN-LAST:event_jButton1ActionPerformed

