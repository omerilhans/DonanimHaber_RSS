    public App() 
    {
        initComponents();
        dtm = (DefaultTableModel) jTable1.getModel();
        try
        {
            String adr= "http://www.donanimhaber.com/rss/tum/";
            Document doc = Jsoup.connect(adr)
                    .timeout(30000)
                    .userAgent("Mozilla")
                    .ignoreContentType(true)
                    .followRedirects(true)
                    .get();
            haberListesi = doc.select("item");
            System.out.println(haberListesi.size()+" Haber Yüklendi");
            
            // Tüm Alınan Haberler İçin
            for (Element haber : haberListesi)
            {
                String baslik = haber.select("title").text();
                String tarih = haber.select("pubDate").text();
                Object row[] = { baslik, tarih };
                dtm.addRow(row);
            }
            
        } catch (Exception e) {e.printStackTrace(); }
    }

